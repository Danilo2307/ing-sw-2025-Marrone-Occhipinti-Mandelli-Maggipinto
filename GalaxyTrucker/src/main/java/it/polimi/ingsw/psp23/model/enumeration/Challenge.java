package it.polimi.ingsw.psp23.model.enumeration;

/**
 * The Challenge enum represents different types of challenges in the card Combat Zone.
 * Each constant corresponds to a specific challenge that may assess varying aspects
 * of a game's ship or crew.
 *
 * The enum constants include:
 * - CannonStrength: Represents challenges related to the strength of cannons.
 * - Members: Represents challenges that involve the crew or members onboard.
 * - EngineStrength: Represents challenges regarding the engine's power.
 * - Goods: Represents challenges pertaining to the handling or storage of goods.
 */
    public enum Challenge {
        CannonStrength,
        Members,
        EngineStrength,
        Goods;
    }
