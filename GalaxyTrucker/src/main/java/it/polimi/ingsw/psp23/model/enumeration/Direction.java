package it.polimi.ingsw.psp23.model.enumeration;

/**
 * The Direction enum represents cardinal directions used for navigation
 * or positioning in a two-dimensional space. Each direction corresponds
 * to one of the four primary movement options:
 *
 * UP: Represents the upward direction.
 * DOWN: Represents the downward direction.
 * LEFT: Represents the leftward direction.
 * RIGHT: Represents the rightward direction.
 */
public enum Direction {
    UP,
    DOWN,
    LEFT,
    RIGHT;
}
